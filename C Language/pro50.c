/**
    arithmetic operators
*/

void main(){
    int ip1, ip2;

    printf("\n Enter any value for ip1 \t");
    scanf("%d", &ip1);
    printf("\n Enter any value for ip2 \t");
    scanf("%d", &ip2);

    printf("\n Sum of %d and %d is %d", ip1, ip2, ip1 + ip2);
    printf("\n Sub of %d and %d is %d", ip1, ip2, ip1 - ip2);
    printf("\n Mul of %d and %d is %d", ip1, ip2, ip1 * ip2);
    printf("\n Div of %d and %d is %d", ip1, ip2, ip1 / ip2);
    printf("\n Mod of %d and %d is %d", ip1, ip2, ip1 % ip2);
}
